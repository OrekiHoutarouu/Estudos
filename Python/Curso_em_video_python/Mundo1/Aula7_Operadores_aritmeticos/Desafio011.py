altura = float(input('Digite a altura da sua parede em metros: '))
largura = float(input('Agora digite a largura da sua parede em metros: '))

print(f'A área da sua parede é de {altura * largura:.2f} metros quadrados, e você precisará de {(altura * largura) / 2:.2f} litros de tinta para pintar essa parede.')